
--  From the table DEPARTMENT and PERSON perform the following queries:  

SELECT *
FROM PERSON

SELECT *
FROM DEPARTMENT

-- Part – A: 


-- 1. Combine information from Person and Department table using cross join or Cartesian product. 

SELECT *
FROM PERSON P
CROSS JOIN DEPARTMENT D

-- 2. Find all persons with their department name.

SELECT PERSONNAME, DEPARTMENTNAME
FROM PERSON P
    JOIN DEPARTMENT D
    ON P.DEPARTMENTID = D.DEPARTMENTID

-- 3. Find all persons with their department name & code. 

SELECT PERSONNAME, DEPARTMENTNAME, DEPARTMENTCODE
FROM PERSON P
    FULL OUTER JOIN DEPARTMENT D
    ON P.DEPARTMENTID = D.DEPARTMENTID

-- 4. Find all persons with their department code and location.

SELECT PERSONNAME, DEPARTMENTCODE, LOCATION
FROM PERSON P
    FULL OUTER JOIN DEPARTMENT D
    ON P.DEPARTMENTID = D.DEPARTMENTID

-- 5. Find the detail of the person who belongs to Mechanical department. 

SELECT *
FROM PERSON P
    FULL OUTER JOIN DEPARTMENT D
    ON P.DEPARTMENTID = D.DEPARTMENTID
WHERE DEPARTMENTNAME = 'MECHANICAL'

-- 6. Final person’s name, department code and salary who lives in Ahmedabad city. 

SELECT PERSONNAME, DEPARTMENTCODE, SALARY
FROM PERSON P
    JOIN DEPARTMENT D
    ON P.DEPARTMENTID = D.DEPARTMENTID
WHERE CITY = 'AHMEDABAD'

-- 7. Find the person's name whose department is in C-Block.

SELECT PERSONNAME
FROM PERSON P
    JOIN DEPARTMENT D
    ON P.DEPARTMENTID = D.DEPARTMENTID
WHERE LOCATION = 'C-BLOCK'

-- 8. Retrieve person name, salary & department name who belongs to Jamnagar city. 

SELECT PERSONNAME, SALARY, DEPARTMENTNAME
FROM PERSON P
    JOIN DEPARTMENT D
    ON P.DEPARTMENTID = D.DEPARTMENTID
WHERE CITY = 'JAMNAGAR'

-- 9. Retrieve person’s detail who joined the Civil department after 1-Aug-2001.

SELECT *
FROM PERSON P
    JOIN DEPARTMENT D
    ON P.DEPARTMENTID = D.DEPARTMENTID
WHERE DEPARTMENTNAME = 'CIVIL' AND JOININGDATE > '2001-08-01'

-- 10. Display all the person's name with the department whose joining date difference with the current date 
-- is more than 25 years. 

SELECT PERSONNAME, DEPARTMENTNAME
FROM PERSON P
    JOIN DEPARTMENT D
    ON P.DEPARTMENTID = D.DEPARTMENTID
WHERE DATEDIFF(YEAR, JOININGDATE, GETDATE()) > 25

-- 11. Find department wise person counts. 

SELECT DEPARTMENTNAME, COUNT(*) AS COUNT_PERSON
FROM PERSON P
    JOIN DEPARTMENT D
    ON P.DEPARTMENTID = D.DEPARTMENTID
GROUP BY DEPARTMENTNAME

-- 12. Give department wise maximum & minimum salary with department name. 

SELECT DEPARTMENTNAME, MAX(SALARY) AS MAX_SALARY, MIN(SALARY) AS MIN_SALARY
FROM PERSON P
    JOIN DEPARTMENT D
    ON P.DEPARTMENTID = D.DEPARTMENTID
GROUP BY DEPARTMENTNAME

-- 13. Find city wise total, average, maximum and minimum salary. 

SELECT CITY, SUM(SALARY) AS TOTAL_SALARY, AVG(SALARY) AS AVG_SALARY, MAX(SALARY) AS MAX_SALARY, MIN(SALARY) AS MIN_SALAR
FROM PERSON P
    JOIN DEPARTMENT D
    ON P.DEPARTMENTID = D.DEPARTMENTID
GROUP BY CITY

-- 14. Find the average salary of a person who belongs to Ahmedabad city. 

SELECT AVG(SALARY) AS AVG_SALARY
FROM PERSON P
WHERE CITY = 'AHMEDABAD'

-- 15. Produce Output Like: <PersonName> lives in <City> and works in <DepartmentName> Department. (In 
-- single column) 

SELECT PERSONNAME + 'LIVES IN' + CITY + 'LIVES IN' + DEPARTMENTNAME
FROM PERSON P
    JOIN DEPARTMENT D
    ON P.DEPARTMENTID = D.DEPARTMENTID


-- Part – B: 


-- 16. Produce Output Like: <PersonName> earns <Salary> from <DepartmentName> department monthly. (In 
-- single column) 

SELECT PERSONNAME + 'EARNS' + CAST(SALARY AS VARCHAR(200)) + 'FROM' + DEPARTMENTNAME
FROM PERSON P
    JOIN DEPARTMENT D
    ON P.DEPARTMENTID = D.DEPARTMENTID

-- 17. Find city & department wise total, average & maximum salaries.

SELECT CITY,DEPARTMENTNAME,SUM(SALARY) AS TOTAL_SALARY,AVG(SALARY) AS AVG_SALARY,MAX(SALARY) AS MAX_SALARY
FROM PERSON P
JOIN DEPARTMENT D
ON P.DEPARTMENTID = D.DEPARTMENTID
GROUP BY CITY,DEPARTMENTNAME

-- 18. Find all persons who do not belong to any department.

SELECT PERSONNAME
FROM PERSON    
WHERE DEPARTMENTID IS NULL

-- 19. Find all departments whose total salary is exceeding 100000. 

SELECT D.DEPARTMENTID,D.DEPARTMENTNAME,SUM(P.SALARY) AS TOTAL_SALARY
FROM PERSON P
JOIN DEPARTMENT D
ON P.DEPARTMENTID = D.DEPARTMENTID
GROUP BY D.DEPARTMENTID,D.DEPARTMENTNAME
HAVING SUM(P.SALARY) > 100000



-- Part – C: 


-- 20. List all departments who have no person. 

SELECT DEPARTMENTNAME,COUNT(PERSONNAME) AS TWO_MORETHAN_WORKING
FROM PERSON P
JOIN DEPARTMENT D
ON P.DEPARTMENTID = D.DEPARTMENTID
GROUP BY DEPARTMENTNAME
HAVING COUNT(PERSONNAME) IS NULL

-- 21. List out department names in which more than two persons are working. 

SELECT DEPARTMENTNAME,COUNT(PERSONNAME) AS TWO_MORETHAN_WORKING
FROM PERSON P
JOIN DEPARTMENT D
ON P.DEPARTMENTID = D.DEPARTMENTID
GROUP BY DEPARTMENTNAME
HAVING COUNT(PERSONNAME) > 2

-- 22. Give a 10% increment in the computer department employee’s salary. (Use Update) 

UPDATE PERSON
SET SALARY = SALARY * 0.10
FROM PERSON P
JOIN DEPARTMENT D
ON P.DEPARTMENTID = D.DEPARTMENTID
WHERE DEPARTMENTNAME = 'COMPUTER'
